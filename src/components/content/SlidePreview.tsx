export function SlidePreview({ title, content, colors, isDark }: SlidePreviewProps) {
  const [currentSlide, setCurrentSlide] = useState(0);
  const [viewMode, setViewMode] = useState<'single' | 'grid'>('single');
  const [isFullscreen, setIsFullscreen] = useState(false);
  const [downloading, setDownloading] = useState(false);
  const [presenting, setPresenting] = useState(false);
  const [showNotes, setShowNotes] = useState(false);
  const [showPresenterNotes, setShowPresenterNotes] = useState(false);

  const { isMobile } = useResponsive();
  const thumbnailStripRef = useRef<HTMLDivElement>(null);
  const touchStartX = useRef<number | null>(null);

  const slides = useMemo(() => parseMarkdownToSlides(content), [content]);
  const totalSlides = slides.length;
  const t = DEFAULT_THEME;

  const goTo = useCallback((idx: number) => {
    if (idx >= 0 && idx < totalSlides) setCurrentSlide(idx);
  }, [totalSlides]);

  const handleDownload = async () => {
    setDownloading(true);
    try { await downloadSlidesAsPptx(title, content); }
    catch (err) { console.error('PPTX download failed:', err); }
    finally { setDownloading(false); }
  };

  // Auto-scroll thumbnail strip to active
  useEffect(() => {
    if (thumbnailStripRef.current) {
      const thumb = thumbnailStripRef.current.children[currentSlide] as HTMLElement;
      if (thumb) thumb.scrollIntoView({ behavior: 'smooth', block: 'nearest', inline: 'center' });
    }
  }, [currentSlide]);

  /* ----- Presentation Mode ----- */
  const startPresentation = useCallback(() => {
    setPresenting(true);
    setShowPresenterNotes(false);
    try { document.documentElement.requestFullscreen?.(); } catch { /* noop */ }
  }, []);

  const stopPresentation = useCallback(() => {
    setPresenting(false);
    try { if (document.fullscreenElement) document.exitFullscreen?.(); } catch { /* noop */ }
  }, []);

  // Keyboard navigation for presentation
  useEffect(() => {
    if (!presenting) return;
    const handler = (e: KeyboardEvent) => {
      if (e.key === 'Escape') { stopPresentation(); return; }
      if (e.key === 'ArrowRight' || e.key === ' ') { e.preventDefault(); goTo(Math.min(currentSlide + 1, totalSlides - 1)); }
      if (e.key === 'ArrowLeft') { e.preventDefault(); goTo(Math.max(currentSlide - 1, 0)); }
      if (e.key === 'n' || e.key === 'N') { setShowPresenterNotes(p => !p); }
    };
    window.addEventListener('keydown', handler);
    return () => window.removeEventListener('keydown', handler);
  }, [presenting, currentSlide, totalSlides, goTo, stopPresentation]);

  // Exit presentation on fullscreen exit
  useEffect(() => {
    const handler = () => { if (!document.fullscreenElement && presenting) setPresenting(false); };
    document.addEventListener('fullscreenchange', handler);
    return () => document.removeEventListener('fullscreenchange', handler);
  }, [presenting]);

  // Touch swipe for presentation mode & mobile
  const handleTouchStart = useCallback((e: React.TouchEvent) => {
    touchStartX.current = e.touches[0].clientX;
  }, []);

  const handleTouchEnd = useCallback((e: React.TouchEvent) => {
    if (touchStartX.current === null) return;
    const dx = e.changedTouches[0].clientX - touchStartX.current;
    if (Math.abs(dx) > 50) {
      if (dx < 0) goTo(Math.min(currentSlide + 1, totalSlides - 1));
      else goTo(Math.max(currentSlide - 1, 0));
    }
    touchStartX.current = null;
  }, [currentSlide, totalSlides, goTo]);

  /* ----- Empty State ----- */
  if (totalSlides === 0) {
    return (
      <div style={{
        display: 'flex', alignItems: 'center', justifyContent: 'center',
        height: '100%', color: colors.textMuted, fontSize: '14px',
        fontFamily: "'Quicksand', sans-serif",
      }}>
        No slide content to preview
      </div>
    );
  }

  /* ----- Presentation Mode Render ----- */
  if (presenting) {
    const currentNotes = slides[currentSlide]?.notes || '';
    return (
      <div
        style={{
          position: 'fixed', inset: 0, zIndex: 99999, backgroundColor: '#000',
          display: 'flex', flexDirection: 'column', alignItems: 'center', justifyContent: 'center',
          cursor: 'none',
        }}
        onTouchStart={handleTouchStart}
        onTouchEnd={handleTouchEnd}
        onClick={(e) => {
          const rect = (e.target as HTMLElement).getBoundingClientRect();
          const x = e.clientX - rect.left;
          if (x > rect.width / 2) goTo(Math.min(currentSlide + 1, totalSlides - 1));
          else goTo(Math.max(currentSlide - 1, 0));
        }}
      >
        {/* Slide */}
        <div style={{
          width: '100vw', height: '100vh', display: 'flex',
          alignItems: 'center', justifyContent: 'center', position: 'relative',
        }}>
          <div style={{
            width: '100%', height: '100%', maxWidth: 'calc(100vh * 16 / 9)',
            aspectRatio: '16/9', position: 'relative', overflow: 'hidden',
            transition: 'opacity 0.3s ease',
          }}>
            <SlideRenderer slide={slides[currentSlide]} slideIndex={currentSlide} totalSlides={totalSlides} title={title} t={t} />
          </div>
        </div>

        {/* Bottom bar */}
        <div style={{
          position: 'fixed', bottom: 0, left: 0, right: 0, height: '40px',
          display: 'flex', alignItems: 'center', justifyContent: 'center', gap: '12px',
          backgroundColor: 'rgba(0,0,0,0.7)', opacity: 0.8,
          transition: 'opacity 0.3s ease',
        }}>
          <span style={{ fontFamily: "'Rajdhani', sans-serif", fontSize: '13px', fontWeight: 600, color: '#FEC00F', letterSpacing: '1px' }}>
            {currentSlide + 1} / {totalSlides}
          </span>
          <button onClick={(e) => { e.stopPropagation(); stopPresentation(); }}
            style={{ background: 'none', border: '1px solid #555', borderRadius: '4px', color: '#999', cursor: 'pointer', padding: '4px 10px', fontSize: '10px', fontFamily: "'Rajdhani', sans-serif", fontWeight: 600, letterSpacing: '0.5px' }}>
            ESC TO EXIT
          </button>
          <button onClick={(e) => { e.stopPropagation(); setShowPresenterNotes(p => !p); }}
            title="Toggle notes (N)"
            style={{
              background: showPresenterNotes ? 'rgba(254,192,15,0.15)' : 'none',
              border: showPresenterNotes ? '1px solid rgba(254,192,15,0.4)' : '1px solid #555',
              borderRadius: '4px', color: showPresenterNotes ? '#FEC00F' : '#999',
              cursor: 'pointer', padding: '4px 8px', display: 'flex', alignItems: 'center',
            }}>
            <StickyNote size={12} />
          </button>
        </div>

        {/* Speaker notes overlay */}
        {showPresenterNotes && currentNotes && (
          <div style={{
            position: 'fixed', bottom: '48px', left: '50%', transform: 'translateX(-50%)',
            maxWidth: '600px', width: '90%', padding: '16px 20px',
            backgroundColor: 'rgba(0,0,0,0.85)', borderRadius: '10px',
            border: '1px solid rgba(254,192,15,0.2)',
          }}
            onClick={(e) => e.stopPropagation()}>
            <div style={{ display: 'flex', alignItems: 'center', gap: '6px', marginBottom: '8px' }}>
              <StickyNote size={12} color="#FEC00F" />
              <span style={{ fontFamily: "'Rajdhani', sans-serif", fontSize: '10px', fontWeight: 700, color: '#FEC00F', letterSpacing: '1px' }}>SPEAKER NOTES</span>
            </div>
            <p style={{ fontFamily: "'Quicksand', sans-serif", fontSize: '13px', color: '#ccc', lineHeight: 1.7, margin: 0, whiteSpace: 'pre-wrap' }}>
              {currentNotes}
            </p>
          </div>
        )}
      </div>
    );
  }

  /* ----- Standard View ----- */
  const currentNotes = slides[currentSlide]?.notes || '';

  const containerStyle: React.CSSProperties = isFullscreen
    ? { position: 'fixed', top: 0, left: 0, right: 0, bottom: 0, zIndex: 9999, backgroundColor: isDark ? '#0a0a0a' : '#f5f5f5', display: 'flex', flexDirection: 'column' }
    : { display: 'flex', flexDirection: 'column', height: '100%' };

  return (
    <div style={containerStyle}>
      {/* ========== Toolbar ========== */}
      <div style={{
        display: 'flex', alignItems: 'center', justifyContent: 'space-between',
        padding: isMobile ? '8px 12px' : '10px 20px',
        borderBottom: `1px solid ${colors.border}`,
        backgroundColor: isDark ? '#111' : '#fafafa', flexShrink: 0,
        gap: '8px',
      }}>
        {/* Left: slide counter */}
        <span style={{
          fontFamily: "'Rajdhani', sans-serif", fontWeight: 700,
          fontSize: '13px', color: colors.primaryYellow,
          letterSpacing: '1px', textTransform: 'uppercase', whiteSpace: 'nowrap',
        }}>
          SLIDE {currentSlide + 1} / {totalSlides}
        </span>

        {/* Center: navigation arrows (single mode only) */}
        {viewMode === 'single' && !isMobile && (
          <div style={{ display: 'flex', alignItems: 'center', gap: '6px' }}>
            <NavButton onClick={() => goTo(currentSlide - 1)} disabled={currentSlide === 0} colors={colors} isDark={isDark}>
              <ChevronLeft size={16} />
            </NavButton>
            <NavButton onClick={() => goTo(currentSlide + 1)} disabled={currentSlide === totalSlides - 1} colors={colors} isDark={isDark}>
              <ChevronRight size={16} />
            </NavButton>
          </div>
        )}

        {/* Right: actions */}
        <div style={{ display: 'flex', alignItems: 'center', gap: '6px' }}>
          {/* Notes toggle */}
          <ToolbarButton
            onClick={() => setShowNotes(!showNotes)}
            active={showNotes}
            colors={colors} isDark={isDark} title="Speaker Notes"
          >
            <StickyNote size={14} />
          </ToolbarButton>

          {!isMobile && (
            <ToolbarButton
              onClick={() => setViewMode(viewMode === 'single' ? 'grid' : 'single')}
              active={viewMode === 'grid'}
              colors={colors} isDark={isDark}
              title={viewMode === 'single' ? 'Grid view' : 'Single view'}
            >
              {viewMode === 'single' ? <Grid size={14} /> : <Layers size={14} />}
            </ToolbarButton>
          )}

          <ToolbarButton
            onClick={() => setIsFullscreen(!isFullscreen)}
            colors={colors} isDark={isDark}
            title={isFullscreen ? 'Exit fullscreen' : 'Fullscreen'}
          >
            {isFullscreen ? <Minimize2 size={14} /> : <Maximize2 size={14} />}
          </ToolbarButton>

          {/* Present button */}
          <button onClick={startPresentation} style={{
            display: 'flex', alignItems: 'center', gap: '6px',
            padding: '7px 14px',
            backgroundColor: isDark ? '#00DED1' : '#00b8ab',
            color: '#1a1a1a', border: 'none', borderRadius: '8px', cursor: 'pointer',
            fontFamily: "'Rajdhani', sans-serif", fontWeight: 700,
            fontSize: '12px', letterSpacing: '0.5px',
          }}>
            <Play size={13} />
            {!isMobile && 'PRESENT'}
          </button>

          <button onClick={handleDownload} disabled={downloading} style={{
            display: 'flex', alignItems: 'center', gap: '6px',
            padding: '7px 14px',
            background: `linear-gradient(135deg, ${colors.primaryYellow}, #FFD700)`,
            color: '#1a1a1a', border: 'none', borderRadius: '8px',
            cursor: downloading ? 'wait' : 'pointer',
            fontFamily: "'Rajdhani', sans-serif", fontWeight: 700,
            fontSize: '12px', letterSpacing: '0.5px',
            opacity: downloading ? 0.7 : 1,
          }}>
            <Download size={13} />
            {!isMobile && (downloading ? 'EXPORTING...' : '.PPTX')}
          </button>
        </div>
      </div>

      {/* ========== Slide Area ========== */}
      <div style={{ flex: 1, overflow: 'hidden', display: 'flex', flexDirection: 'column' }}>
        <div
          style={{
            flex: 1, overflow: 'auto',
            display: 'flex', alignItems: 'center', justifyContent: 'center',
            padding: viewMode === 'grid' ? '20px' : (isMobile ? '12px' : '24px'),
            backgroundColor: isDark ? '#0a0a0a' : '#e8e8e8',
          }}
          onTouchStart={viewMode === 'single' ? handleTouchStart : undefined}
          onTouchEnd={viewMode === 'single' ? handleTouchEnd : undefined}
        >
          {viewMode === 'single' ? (
            <div style={{
              width: '100%',
              maxWidth: isFullscreen ? '1100px' : '820px',
              aspectRatio: '16 / 9',
              borderRadius: '8px',
              boxShadow: '0 8px 32px rgba(0,0,0,0.4)',
              overflow: 'hidden',
              position: 'relative',
            }}>
              <SlideRenderer
                slide={slides[currentSlide]}
                slideIndex={currentSlide}
                totalSlides={totalSlides}
                title={title}
                t={t}
              />
            </div>
          ) : (
            <GridView
              slides={slides}
              currentSlide={currentSlide}
              title={title}
              colors={colors}
              isDark={isDark}
              t={t}
              onSelect={(i) => { setCurrentSlide(i); setViewMode('single'); }}
            />
          )}
        </div>

        {/* ========== Thumbnail Navigator Strip (single mode) ========== */}
        {viewMode === 'single' && (
          <div style={{
            flexShrink: 0, borderTop: `1px solid ${colors.border}`,
            backgroundColor: isDark ? '#111' : '#f5f5f5',
            padding: '8px 12px',
          }}>
            <div
              ref={thumbnailStripRef}
              style={{
                display: 'flex', gap: '8px', overflowX: 'auto',
                WebkitOverflowScrolling: 'touch', scrollbarWidth: 'none',
                paddingBottom: '4px',
              }}
            >
              {slides.map((slide, i) => {
                const isActive = i === currentSlide;
                return (
                  <button
                    key={i}
                    onClick={() => goTo(i)}
                    style={{
                      flexShrink: 0,
                      width: isMobile ? '72px' : '88px',
                      aspectRatio: '16/9',
                      borderRadius: '4px',
                      border: isActive ? `2px solid ${colors.primaryYellow}` : `1px solid ${isDark ? '#333' : '#ccc'}`,
                      overflow: 'hidden',
                      cursor: 'pointer',
                      position: 'relative',
                      backgroundColor: t.bg,
                      boxShadow: isActive ? `0 0 8px ${colors.primaryYellow}30` : 'none',
                      transition: 'all 0.15s ease',
                      padding: 0,
                    }}
                  >
                    {/* Mini render */}
                    <div style={{ transform: 'scale(0.12)', transformOrigin: 'top left', width: `${100/0.12}%`, height: `${100/0.12}%`, pointerEvents: 'none' }}>
                      <SlideRenderer slide={slide} slideIndex={i} totalSlides={totalSlides} title={title} t={t} />
                    </div>
                    {/* Slide number badge */}
                    <div style={{
                      position: 'absolute', bottom: '2px', right: '4px',
                      fontFamily: "'Rajdhani', sans-serif", fontSize: '8px', fontWeight: 700,
                      color: isActive ? colors.primaryYellow : (isDark ? '#555' : '#999'),
                    }}>{i + 1}</div>
                  </button>
                );
              })}
            </div>
          </div>
        )}

        {/* ========== Notes Tray ========== */}
        {showNotes && viewMode === 'single' && (
          <div style={{
            flexShrink: 0, borderTop: `1px solid ${colors.border}`,
            backgroundColor: isDark ? '#111' : '#fafafa',
            padding: '12px 20px', maxHeight: '150px', overflowY: 'auto',
          }}>
            <div style={{ display: 'flex', alignItems: 'center', gap: '6px', marginBottom: '6px' }}>
              <StickyNote size={12} color={colors.primaryYellow} />
              <span style={{
                fontFamily: "'Rajdhani', sans-serif", fontSize: '10px', fontWeight: 700,
                color: colors.primaryYellow, letterSpacing: '1px',
              }}>SPEAKER NOTES</span>
            </div>
            {currentNotes ? (
              <p style={{
                fontFamily: "'Quicksand', sans-serif", fontSize: '12px',
                color: colors.text, lineHeight: 1.7, margin: 0, whiteSpace: 'pre-wrap',
              }}>{currentNotes}</p>
            ) : (
              <p style={{
                fontFamily: "'Quicksand', sans-serif", fontSize: '12px',
                color: colors.textMuted, lineHeight: 1.7, margin: 0, fontStyle: 'italic',
              }}>No notes for this slide.</p>
            )}
          </div>
        )}

        {/* Mobile nav arrows */}
        {isMobile && viewMode === 'single' && (
          <div style={{
            flexShrink: 0, display: 'flex', borderTop: `1px solid ${colors.border}`,
            backgroundColor: isDark ? '#111' : '#fafafa',
          }}>
            <button onClick={() => goTo(currentSlide - 1)} disabled={currentSlide === 0}
              style={{
                flex: 1, padding: '12px', display: 'flex', alignItems: 'center', justifyContent: 'center',
                background: 'none', border: 'none', cursor: currentSlide === 0 ? 'not-allowed' : 'pointer',
                color: currentSlide === 0 ? colors.borderSubtle : colors.textMuted, minHeight: '48px',
              }}>
              <ChevronLeft size={20} />
            </button>
            <div style={{ display: 'flex', alignItems: 'center', padding: '0 16px' }}>
              <span style={{ fontFamily: "'Rajdhani', sans-serif", fontWeight: 700, fontSize: '13px', color: colors.textMuted }}>
                {currentSlide + 1} / {totalSlides}
              </span>
            </div>
            <button onClick={() => goTo(currentSlide + 1)} disabled={currentSlide === totalSlides - 1}
              style={{
                flex: 1, padding: '12px', display: 'flex', alignItems: 'center', justifyContent: 'center',
                background: 'none', border: 'none', cursor: currentSlide === totalSlides - 1 ? 'not-allowed' : 'pointer',
                color: currentSlide === totalSlides - 1 ? colors.borderSubtle : colors.textMuted, minHeight: '48px',
              }}>
              <ChevronRight size={20} />
            </button>
          </div>
        )}
      </div>
    </div>
  );
}

