function GridView({ slides, currentSlide, title, colors, isDark, t, onSelect }: {
  slides: ParsedSlide[]; currentSlide: number; title: string;
  colors: Record<string, string>; isDark: boolean; t: ThemeColors;
  onSelect: (i: number) => void;
}) {
  return (
    <div style={{
      display: 'grid', gridTemplateColumns: 'repeat(auto-fill, minmax(260px, 1fr))',
      gap: '16px', width: '100%', maxWidth: '1200px',
    }}>
      {slides.map((slide, i) => {
        const isActive = i === currentSlide;
        return (
          <button key={i} onClick={() => onSelect(i)}
            style={{
              aspectRatio: '16 / 9', borderRadius: '6px',
              border: isActive ? `2px solid ${t.accent}` : '2px solid transparent',
              overflow: 'hidden', cursor: 'pointer', position: 'relative',
              textAlign: 'left', transition: 'all 0.15s ease', padding: 0,
              boxShadow: isActive ? `0 4px 16px ${t.accent}30` : '0 2px 8px rgba(0,0,0,0.3)',
            }}>
            <SlideRenderer slide={slide} slideIndex={i} totalSlides={slides.length} title={title} t={t} />
            <div style={{
              position: 'absolute', bottom: '22px', right: '8px',
              fontFamily: "'Rajdhani', sans-serif", fontSize: '10px', fontWeight: 700,
              color: isActive ? t.accent : t.mutedColor,
            }}>{i + 1}</div>
          </button>
        );
      })}
    </div>
  );
}

